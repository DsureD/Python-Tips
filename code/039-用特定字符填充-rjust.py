a = "a"
print(a.rjust(5, "x"))  # xxxxa
print(a.ljust(5, "x"))  # axxxx

s1 = 'hello world'
print(f'{s1:^20}')  # *:用星号字符填充；^:居中对齐（<左对齐，>右对齐）；20:宽度20（如果s1超过20个字符串，就没有任何填充字符）
print(f'{s1:*^20}')  # *:用星号字符填充；^:居中对齐（<左对齐，>右对齐）；20:宽度20（如果s1超过20个字符串，就没有任何填充字符）
print(f'{s1:0>20}')  # 右对齐，s1不足20位时，左边用0填充
